public class Aufgabge2ArithmetischeOperation {
    public static void main(String[] args) {
        int a = 3;
        int b = 2;
        int Ergebnis;

        Ergebnis = a + b;
        System.out.println("a +  b=" +  Ergebnis);
        Ergebnis = a - b;
        System.out.println("a -  b=" + Ergebnis);
        Ergebnis = a * b;
        System.out.println("a *  b=" + Ergebnis);
        Ergebnis = a / b;
        System.out.println("a /  b=" + Ergebnis);
        Ergebnis = a % b;
        System.out.println("a Modulo  b=" + Ergebnis);



    }
}
